// JS LEARNING DAY 04 - Switch Case and Break/Continue

// What is a Switch Case?
// A switch case is a control flow statement that allows you to execute different blocks of code based on the value of a variable or expression.
// It is an alternative to using multiple if-else statements and can make your code more readable and easier to maintain.


// The syntax of a switch case is as follows:
// switch (expression) {
//     case value1: 
//         // code to be executed if expression === value1
//         break;
//     case value2:
//         // code to be executed if expression === value2
//         break;
//     ...
//     default:
//         // code to be executed if expression doesn't match any case
// }


// What is Break and Continue?
// Break and continue are control flow statements that are used to alter the flow of a loop or a switch case.   
// Break is used to exit a loop or a switch case prematurely, while continue is used to skip the current iteration of a loop and move on to the next one.

// The syntax of break and continue is as follows:
// break; // exits the current loop or switch case
// continue; // skips the current iteration of a loop and moves on to the next one