// JS LEARNING DAY 03 - For Loop and While Loop

// What is a Loop?
// A loop is a programming structure that allows you to repeat a block of code multiple times.
//  It helps you to automate repetitive tasks and can be used to iterate over arrays, objects, or any other data structures.


// For Loop
// A for loop is a control flow statement that allows you to execute a block of code a specific number of times.
// The syntax of a for loop is as follows:
// for (initialization; condition; increment/decrement) {
//     // code to be executed
// }









// while Loop
// A while loop is a control flow statement that allows you to execute a block of code as long as a specified condition is true.
// The syntax of a while loop is as follows:
// while (condition) {
//     // code to be executed
// }